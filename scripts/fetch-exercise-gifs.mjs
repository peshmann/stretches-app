function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function searchTenor(query) {
  const params = new URLSearchParams({
    q: query,
    key: TENOR_API_KEY,
    limit: '5',
    media_filter: 'gif',
  });

  const url = `${TENOR_SEARCH_URL}?${params}`;
  const response = await fetch(url);

  if (!response.ok) {
    throw new Error(`Tenor API error: ${response.status} ${response.statusText}`);
  }

  return response.json();
}

async function main() {
  const mapping = {};
  const searchCache = {}; // Cache results for duplicate search terms
  const notFound = [];

  console.log(`Fetching GIFs for ${exercises.length} exercises...\n`);

  for (const exercise of exercises) {
    // Reuse cached result for duplicate search terms
    if (searchCache[exercise.search]) {
      mapping[exercise.id] = searchCache[exercise.search];
      console.log(`  [REUSE] ${exercise.id} -> reused from cache (same search: "${exercise.search}")`);
      continue;
    }

    try {
      const data = await searchTenor(exercise.search);

      if (data.results && data.results.length > 0) {
        const gifUrl = data.results[0].media_formats?.gif?.url;
        if (gifUrl) {
          mapping[exercise.id] = gifUrl;
          searchCache[exercise.search] = gifUrl;
          console.log(`  [OK]    ${exercise.id} -> found GIF`);
        } else {
          notFound.push(exercise.id);
          console.log(`  [MISS]  ${exercise.id} -> no gif format in result`);
        }
      } else {
        notFound.push(exercise.id);
        console.log(`  [MISS]  ${exercise.id} -> no results for "${exercise.search}"`);
      }
    } catch (err) {
      notFound.push(exercise.id);
      console.log(`  [ERR]   ${exercise.id} -> ${err.message}`);
    }

    // Rate limit: 500ms between API calls
    await sleep(500);
  }

  // Generate TypeScript file
  const lines = [
    '// Auto-generated by scripts/fetch-exercise-gifs.mjs',
    '// To regenerate: node scripts/fetch-exercise-gifs.mjs',
    '',
    'export const exerciseGifs: Record<string, string> = {',
  ];

  for (const [id, url] of Object.entries(mapping)) {
    lines.push(`  '${id}': '${url}',`);
  }

  lines.push('};');
  lines.push('');

  const outputPath = new URL('../src/data/exerciseGifs.ts', import.meta.url);
  const fs = await import('node:fs');
  fs.writeFileSync(outputPath, lines.join('\n'), 'utf-8');

  console.log(`\nWrote ${Object.keys(mapping).length} GIF mappings to src/data/exerciseGifs.ts`);

  if (notFound.length > 0) {
    console.log(`\nExercises with NO GIF found (${notFound.length}):`);
    notFound.forEach((id) => console.log(`  - ${id}`));
  } else {
    console.log('\nAll exercises matched!');
  }
}

main().catch((err) => {
  console.error('Fatal error:', err);
  process.exit(1);
});
